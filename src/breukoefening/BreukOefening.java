package breukoefening;

/**
 *
 * @author vantichelen
 */
public class BreukOefening {
    private int teller;
    private int noemer;


    public BreukOefening(int teller, int noemer) {
        this.teller = teller;
        this.noemer = noemer;
    }

    /**
     * @return the teller
     */
    public int getTeller() {
        return teller;
    }

    /**
     * @param teller the teller to set
     */
    public void setTeller(int teller) {
        this.teller = teller;
    }

    /**
     * @return the noemer
     */
    public int getNoemer() {
        return noemer;
    }

    /**
     * @param noemer the noemer to set
     */
    public void setNoemer(int noemer) {
        this.noemer = noemer;
    }
    
    
    
    
}
